# COORDINATE GENERATOR
# -------------------------------------------------------------------------------------------------
FILES = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h']
RANKS = [1, 2, 3, 4, 5, 6, 7, 8]

print("\"-\"")
for rank in reversed(RANKS):
  for file in FILES:
    print(f"\"{file}{rank}\",")
